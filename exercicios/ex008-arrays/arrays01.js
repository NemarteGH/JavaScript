// Declaração e funcionalidades de listas

let num = [8,1,3,4] // Define o array (lista).

console.log(num) // Mostra a lista.
num[4] = 5 // Adicionando valor em uma posição específica.
num.push(6) // Adicionando valor no final da lista.
console.log(num.length) // Pega a quantidade de itens que tem na lista.
console.log(num.sort()) // Organiza os números
console.log(num[0]) // Mostra valor em índice específico da lista.
console.log(num.indexOf(3)) // Procura pelo valor 2 e retorna o indice, caso o valor não exista, retorna -1.